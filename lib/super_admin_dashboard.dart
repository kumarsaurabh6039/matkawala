// --- GLOBAL PDF HELPER (shared across super admin pages) ---
Future<void> generateAndDownloadPdf({
  required BuildContext context,
  required String title,
  required List<List<String>> headers,
  required List<List<String>> rows,
  List<String>? summaryLines,
}) async {
  final pdf = pw.Document();
  pdf.addPage(
    pw.MultiPage(
      pageFormat: PdfPageFormat.a4,
      build: (pw.Context ctx) => [
        pw.Center(child: pw.Text(title, style: pw.TextStyle(fontSize: 18, fontWeight: pw.FontWeight.bold))),
        pw.SizedBox(height: 8),
        pw.Text("Date: ${DateFormat('dd-MM-yyyy').format(DateTime.now())}", style: const pw.TextStyle(fontSize: 10, color: PdfColors.grey600)),
        pw.SizedBox(height: 12),
        pw.Table(
          border: pw.TableBorder.all(color: PdfColors.grey400, width: 0.5),
          children: [
            pw.TableRow(
              decoration: const pw.BoxDecoration(color: PdfColors.blueGrey800),
              children: headers[0].map((h) => pw.Padding(
                padding: const pw.EdgeInsets.all(6),
                child: pw.Text(h, style: pw.TextStyle(color: PdfColors.white, fontWeight: pw.FontWeight.bold, fontSize: 10)),
              )).toList(),
            ),
            ...rows.asMap().entries.map((entry) => pw.TableRow(
              decoration: pw.BoxDecoration(color: entry.key % 2 == 0 ? PdfColors.white : PdfColors.grey100),
              children: entry.value.map((cell) => pw.Padding(
                padding: const pw.EdgeInsets.all(5),
                child: pw.Text(cell, style: const pw.TextStyle(fontSize: 9)),
              )).toList(),
            )),
          ],
        ),
        if (summaryLines != null) ...[
          pw.SizedBox(height: 16),
          pw.Container(
            padding: const pw.EdgeInsets.all(10),
            decoration: const pw.BoxDecoration(color: PdfColors.blueGrey50),
            child: pw.Column(
              crossAxisAlignment: pw.CrossAxisAlignment.start,
              children: summaryLines.map((line) => pw.Text(line, style: const pw.TextStyle(fontSize: 10))).toList(),
            ),
          ),
        ],
        pw.SizedBox(height: 20),
        pw.Center(child: pw.Text("* Generated by MATKAWALA Super Admin Panel *", style: const pw.TextStyle(fontSize: 8, color: PdfColors.grey))),
      ],
    ),
  );
  await Printing.layoutPdf(
    onLayout: (format) async => pdf.save(),
    name: '${title.replaceAll(' ', '_')}_${DateFormat('dd-MM-yyyy').format(DateTime.now())}.pdf',
  );
}

